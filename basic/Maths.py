import math
# find min value among
x=min(5,10,15)
print(x)
# max value among
y=max(5,1,50,35)
print(y)
# return absolute positive value
x=abs(-1.23)
print(x)

# return the power of a value
x=pow(5,3)
print(x)
# square root of number
y=math.sqrt(4)
print(y)

# rounds a number in upwards to its nearest integer
x=math.ceil(2.05)
# rounds a number in downwards to its nearest integer
y=math.floor(2.05)
print(x)
print(y)
# return the value of PI(i.e 3.141..)
pi=math.pi
print(pi)
